    private void generar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generar2ActionPerformed
        int x,y,z,cant,i=0;
        JList lista;
        Vector X=new Vector();
        
        x=(171*Integer.parseInt(this.wi1.getText()))%30269;
        y=(172*Integer.parseInt(this.wi2.getText()))%30307;
        z=(170*Integer.parseInt(this.wi3.getText()))%30323;
        cant=Integer.parseInt(this.iteracion1.getText());
        
        u=area.format(((double)x/30269)+((double)y/30307)+((double)z/30323));
        X.add(u);
        
        while(i<=cant){
            i++;
            x=(171*x)%30269;
            y=(172*y)%30307;
            z=(170*z)%30323;        
        
            u=area.format(((double)x/30269)+((double)y/30307)+((double)z/30323));
            X.add(u);
        }
        
        lista=new JList(X);
        panel2.setViewportView(lista);
    }//GEN-LAST:event_generar2ActionPerformed

    private void generar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generar3ActionPerformed
        int x,y,z,cant,i=0;
        JList lista;
        Vector X=new Vector();
        
        x=(40014*Integer.parseInt(this.le1.getText()))%2147483563;
        y=(40692*Integer.parseInt(this.le2.getText()))%2147483399;  
        z=(x-y)%2147483562;
        cant=Integer.parseInt(this.iteracion2.getText());
        
        while(i<=cant){
            i++;
            if(z>=0)
                u=area.format(((double)z/2147483563));
            if(z<0)
                u=area.format((double)2147483562/2147483563);
            x=(40014*x)%2147483563;
            y=(40692*y)%2147483399;  
            z=(x-y)%2147483562;
            X.add(u);
        }
        
        lista=new JList(X);
        panel3.setViewportView(lista);
    }//GEN-LAST:event_generar3ActionPerformed

