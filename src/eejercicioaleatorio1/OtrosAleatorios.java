    private void generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarActionPerformed
        String c,numero;
        int f, fibo1=1, fibo2=1;
        JList lista;
        Vector X=new Vector();
        
        c=this.fibon.getText();
        f=Integer.parseInt(c);
        if(f>1){
            numero="Los " + f + " primeros t√©rminos de la serie de Fibonacci son:";
            X.add(numero);
            
            numero=String.valueOf(fibo1)+", ";
             for(int i=2;i<=f;i++){
                numero=numero+String.valueOf(fibo2)+", ";
                fibo2 = fibo1 + fibo2;
                fibo1 = fibo2 - fibo1;
            }
             X.add(numero);
            lista=new JList(X);
            panel.setViewportView(lista);        
        }else {
            JOptionPane.showMessageDialog(null,"Solo ingrese numeros mayores a 1"); 
            fibon.setText("");
        }
    }//GEN-LAST:event_generarActionPerformed

